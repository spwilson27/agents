fn run_agents(root: &Path, bin_dir: &Path, args: &[&str]) -> std::process::Output {
    run_agents_with_env(root, bin_dir, args, &[])
}

fn run_agents_with_env(
    root: &Path,
    bin_dir: &Path,
    args: &[&str],
    extra_env: &[(&str, &str)],
) -> std::process::Output {
    let path = format!(
        "{}:{}",
        bin_dir.display(),
        std::env::var("PATH").expect("PATH should be set")
    );

    let mut command = Command::new(env!("CARGO_BIN_EXE_agents"));
    command
        .args(args)
        .current_dir(root)
        .env("PATH", path)
        .env("EDITOR", bin_dir.join("editor"));

    if bin_dir.join("codex").is_file() {
        command.env("AGENTS_CODEX_BIN", bin_dir.join("codex"));
    }

    for (key, value) in extra_env {
        command.env(key, value);
    }

    command.output().expect("agents binary should run")
}

fn run_in_repo(root: &Path, args: &[&str]) {
    let status = Command::new(args[0])
        .args(&args[1..])
        .current_dir(root)
        .status()
        .expect("command should run");
    assert!(status.success(), "command failed: {}", args.join(" "));
}

fn write_executable(path: &PathBuf, contents: &str) {
    fs::write(path, contents).expect("script should be written");
    let mut permissions = fs::metadata(path)
        .expect("script metadata should exist")
        .permissions();
    permissions.set_mode(0o755);
    fs::set_permissions(path, permissions).expect("script should be executable");
}
