#[test]
fn commit_runs_agent_opens_editor_and_commits_non_empty_message() {
    let temp = tempdir().expect("tempdir should be created");
    let root = temp.path();
    let bin_dir = root.join("bin");
    fs::create_dir(&bin_dir).expect("bin dir should be created");

    write_executable(
        &bin_dir.join("git"),
        r#"#!/bin/sh
set -eu
log_dir=$(dirname "$0")/../logs
mkdir -p "$log_dir"
printf '%s\n' "$*" >> "$log_dir/git.log"
if [ "$1" = "diff" ] && [ "$2" = "--cached" ]; then
  if [ "${3:-}" = "--name-only" ]; then
    printf 'src/main.rs\n'
    exit 0
  fi
  if [ "${3:-}" = "--numstat" ]; then
    printf '1\t0\tsrc/main.rs\n'
    exit 0
  fi
  printf 'diff --git a/src/main.rs b/src/main.rs\n+new line\n'
  exit 0
fi
if [ "$1" = "show" ] && [ "$2" = ":src/main.rs" ]; then
  printf 'fn main() {\n    println!("hello");\n}\n'
  exit 0
fi
if [ "$1" = "commit" ] && [ "$2" = "--file" ]; then
  cp "$3" "$log_dir/raw-commit-message.txt"
  awk '
    /^#/ { next }
    { lines[++count] = $0 }
    END {
      while (count > 0 && lines[count] == "") {
        count--
      }
      for (i = 1; i <= count; i++) {
        print lines[i]
      }
    }
  ' "$3" > "$log_dir/committed-message.txt"
  exit 0
fi
echo "unexpected git invocation: $*" >&2
exit 1
"#,
    );

    write_executable(
        &bin_dir.join("codex"),
        r#"#!/bin/sh
set -eu
log_dir=$(dirname "$0")/../logs
mkdir -p "$log_dir"
printf '%s\n' "$*" > "$log_dir/agent-args.txt"
cat > "$log_dir/agent-prompt.txt"
output_file=""
prev=""
for arg in "$@"; do
  if [ "$prev" = "-o" ]; then
    output_file="$arg"
    break
  fi
  prev="$arg"
done
printf 'feat: add rust commit workflow\n\nGenerated by fake codex.\n' > "$output_file"
printf '{"type":"item.completed","item":{"type":"agent_message","text":"feat: add rust commit workflow\n\nGenerated by fake codex."}}\n'
"#,
    );

    write_executable(
        &bin_dir.join("editor"),
        r#"#!/bin/sh
set -eu
log_dir=$(dirname "$0")/../logs
mkdir -p "$log_dir"
cp "$1" "$log_dir/editor-input.txt"
printf 'feat: finalize commit flow\n\nEdited in fake editor.\n' > "$1"
"#,
    );

    let output = run_agents(
        root,
        &bin_dir,
        &[
            "commit",
            "--cli",
            "codex",
            "--root",
            root.to_str().expect("root path should be utf-8"),
        ],
    );

    assert!(output.status.success(), "stderr: {}", String::from_utf8_lossy(&output.stderr));
    assert!(String::from_utf8_lossy(&output.stdout).trim().is_empty());

    let prompt = fs::read_to_string(root.join("logs/agent-prompt.txt")).expect("agent prompt should exist");
    assert!(prompt.contains("Return only the commit message text"));
    assert!(prompt.contains("diff --git a/src/main.rs b/src/main.rs"));
    assert!(prompt.contains("Do not run tools or inspect the repository"));
    assert!(prompt.contains("Staged file contents:"));
    assert!(prompt.contains("File: src/main.rs"));
    assert!(prompt.contains("println!(\"hello\");"));
    let agent_args = fs::read_to_string(root.join("logs/agent-args.txt")).expect("agent args should exist");
    assert!(agent_args.contains("exec --json --skip-git-repo-check"));
    assert!(agent_args.contains("--sandbox read-only"));
    assert!(agent_args.contains("-o"));

    let committed_message =
        fs::read_to_string(root.join("logs/committed-message.txt")).expect("commit message should be captured");
    assert_eq!(committed_message, "feat: finalize commit flow\n\nEdited in fake editor.\n");
    let editor_input =
        fs::read_to_string(root.join("logs/editor-input.txt")).expect("editor input should be captured");
    assert!(editor_input.starts_with("feat: add rust commit workflow\n\nGenerated by fake codex.\n\n#\n"));
    assert!(editor_input.contains("# Changes to be committed:\n"));
    assert!(editor_input.contains("# src/main.rs | 1 lines changed (+1 -0)\n"));
    let raw_message =
        fs::read_to_string(root.join("logs/raw-commit-message.txt")).expect("raw commit message should be captured");
    assert_eq!(raw_message, "feat: finalize commit flow\n\nEdited in fake editor.\n");

    let git_log = fs::read_to_string(root.join("logs/git.log")).expect("git log should exist");
    assert!(git_log.contains("diff --cached"));
    assert!(git_log.contains("diff --cached --numstat --diff-filter=ACMRD"));
    assert!(git_log.contains("commit --file"));
}

#[test]
fn commit_aborts_when_editor_clears_message() {
    let temp = tempdir().expect("tempdir should be created");
    let root = temp.path();
    let bin_dir = root.join("bin");
    fs::create_dir(&bin_dir).expect("bin dir should be created");

    write_executable(
        &bin_dir.join("git"),
        r#"#!/bin/sh
set -eu
log_dir=$(dirname "$0")/../logs
mkdir -p "$log_dir"
printf '%s\n' "$*" >> "$log_dir/git.log"
if [ "$1" = "diff" ] && [ "$2" = "--cached" ]; then
  if [ "${3:-}" = "--name-only" ]; then
    printf 'README.md\n'
    exit 0
  fi
  if [ "${3:-}" = "--numstat" ]; then
    printf '1\t0\tREADME.md\n'
    exit 0
  fi
  printf 'diff --git a/README.md b/README.md\n+docs\n'
  exit 0
fi
if [ "$1" = "show" ] && [ "$2" = ":README.md" ]; then
  printf '# Agents\n'
  exit 0
fi
if [ "$1" = "commit" ]; then
  echo "git commit should not be called" >&2
  exit 1
fi
echo "unexpected git invocation: $*" >&2
exit 1
"#,
    );

    write_executable(
        &bin_dir.join("qwen"),
        r#"#!/bin/sh
set -eu
cat > /dev/null
printf '{"type":"result","result":"docs: update README"}\n'
"#,
    );

    write_executable(
        &bin_dir.join("editor"),
        r#"#!/bin/sh
set -eu
: > "$1"
"#,
    );

    let output = run_agents(
        root,
        &bin_dir,
        &[
            "commit",
            "--cli",
            "qwen",
            "--root",
            root.to_str().expect("root path should be utf-8"),
        ],
    );

    assert!(output.status.success(), "stderr: {}", String::from_utf8_lossy(&output.stderr));
    assert_eq!(
        String::from_utf8_lossy(&output.stdout).trim(),
        "message empty, aborting commit"
    );

    let git_log = fs::read_to_string(root.join("logs/git.log")).expect("git log should exist");
    assert!(git_log.contains("diff --cached"));
    assert!(!git_log.contains("commit --file"));
}

