fn commit_times_out_for_unresponsive_codex_agent() {
    let temp = tempdir().expect("tempdir should be created");
    let root = temp.path();
    let bin_dir = root.join("bin");
    let src_dir = root.join("src");

    fs::create_dir(&bin_dir).expect("bin dir should be created");
    fs::create_dir(&src_dir).expect("src dir should be created");

    run_in_repo(root, &["git", "init"]);
    run_in_repo(root, &["git", "config", "user.name", "Test User"]);
    run_in_repo(root, &["git", "config", "user.email", "test@example.com"]);

    let file_path = src_dir.join("main.rs");
    fs::write(&file_path, "fn main() {\n    println!(\"before\");\n}\n")
        .expect("initial file should be written");
    run_in_repo(root, &["git", "add", "src/main.rs"]);
    run_in_repo(root, &["git", "commit", "-m", "initial"]);

    fs::write(&file_path, "fn main() {\n    println!(\"after\");\n}\n")
        .expect("updated file should be written");
    run_in_repo(root, &["git", "add", "src/main.rs"]);

    write_executable(
        &bin_dir.join("codex"),
        r#"#!/bin/sh
set -eu
cat > /dev/null
sleep 5
"#,
    );

    write_executable(
        &bin_dir.join("editor"),
        r#"#!/bin/sh
set -eu
printf 'should not run\n' > "$1"
"#,
    );

    let output = run_agents_with_env(
        root,
        &bin_dir,
        &[
            "commit",
            "--cli",
            "codex",
            "--root",
            root.to_str().expect("root path should be utf-8"),
        ],
        &[("AGENTS_TIMEOUT_SECS", "1")],
    );

    assert!(!output.status.success());
    let stderr = String::from_utf8_lossy(&output.stderr);
    assert!(stderr.contains("asking codex for a commit message (1s timeout)..."));
    assert!(stderr.contains("codex timed out after 1s"));
    assert!(!stderr.contains("opening $EDITOR..."));
}

